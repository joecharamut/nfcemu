//Byte 14 (value=0x80=1024/8) specifies tag size in bytes divided by 8
//Byte 18, 19 specify NDEF-Message size, Byte 20..35 is NDEF-header, content starts at Byte 36

NfcEmu::Emulator emu;

int main() {
  // disable clock prescaler
  _PROTECTED_WRITE(CLKCTRL.MCLKCTRLB, 0x00);

  Serial.begin();

  uint16_t crc = CRC_A_INITIAL;
  crc = crc16_iso14443_3_a(crc, 0);
  crc = crc16_iso14443_3_a(crc, 0);
  Serial.print(crc, HEX);
  if (crc == 0x1EA0) {
    Serial.print("\ncrc works\n");
  } else {
    Serial.print("aaaa\n");
  }

  emu.setup(tagStorage, sizeof(tagStorage));
  if (emu.setUid(tagUid, sizeof(tagUid)) < 0) {
    Serial.print("Invalid UID size\n");
  }
  Serial.print("Hello world\n");
  
  while (1) {
    emu.waitForReader();
  }
}
